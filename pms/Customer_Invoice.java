    private void jUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jUpdateActionPerformed
        // TODO add your handling code here:
        String batchno;
        String mcode ;
        String qty ;
        int i;
        int rows_affected =0;

        try {
            Connection con = Pharmacy_Management_System.getConnection();
            Statement stmt = con.createStatement();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            for ( i = 0; i < model.getRowCount(); i++) {
                batchno = model.getValueAt(i, 0).toString();
                mcode = model.getValueAt(i, 1).toString();
                qty = model.getValueAt(i, 3).toString();

                System.out.println(batchno+" "+mcode+"  "+qty);
                System.out.println(model.getRowCount());

                // Update stock table with adjusted quantity
                String updateQuery = "UPDATE stock SET qty = qty - " + qty + " WHERE mcode = '" + mcode + "' AND batchno = '" + batchno + "'";
                rows_affected = stmt.executeUpdate(updateQuery);

            }
            if(rows_affected ==1){
                JOptionPane.showMessageDialog(this, "Database updated");
            }

        } catch (SQLException e) {
            e.printStackTrace();
            System.err.println("Error updating database: " + e.getMessage());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jUpdateActionPerformed

    private void jremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jremoveActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int[] selectedRows = jTable1.getSelectedRows();

        for (int i = selectedRows.length - 1; i >= 0; i--) {//if multiple rows are selected it start from last selected row to first selected row
            model.removeRow(selectedRows[i]);
        }

        double sum = 0.0;

        for (int i = 0; i < model.getRowCount(); i++) {
            sum = sum+ Double.parseDouble(model.getValueAt(i, 5).toString());
            // Display the sum
            jtotal.setText(String.valueOf(sum));
        }
    }//GEN-LAST:event_jremoveActionPerformed

