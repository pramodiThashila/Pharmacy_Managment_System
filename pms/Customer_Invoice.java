    private void jbatchnoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jbatchnoKeyPressed
        // TODO add your handling code here:
        Connection con = null;
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            String mcode = jmcode.getText();
            String batchno = jbatchno.getText();

            try {
                con = Pharmacy_Management_System.getConnection();
                Statement stmt = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);

                ResultSet rs = stmt.executeQuery("SELECT s.s_price, d.name FROM stock s INNER JOIN drugs d ON s.mcode = d.mcode WHERE s.mcode = '" + mcode + "' AND s.batchno = '"+ batchno+"'");

                rs.beforeFirst();

                if(rs.next () == false)
                {
                    JOptionPane.showMessageDialog(this, "mcode or batchno invalied");
                }
                else{
                    double price=rs.getDouble(1);
                    String name=rs.getString(2);

                    junitprice.setText(String.valueOf(price));
                    jname.setText(name);

                }

                if (con != null) {
                    System.out.println("Connected to the database!");
                }
            } catch (SQLException e) {
                e.printStackTrace();
                System.err.println("Error connecting to the database: " + e.getMessage());
            }catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jbatchnoKeyPressed

