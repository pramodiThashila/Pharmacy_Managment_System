        private double calculateTotal(DefaultTableModel DTM)
    {
        double total = 0.0;
        
        for(int i = 0; i < DTM.getRowCount(); i++)
        {
            String stringValue = DTM.getValueAt(i, 5).toString();
            
            double DoubleValue = Double.parseDouble(stringValue);
            
            total = total + DoubleValue;
        }
        
        return total;
    }
    
