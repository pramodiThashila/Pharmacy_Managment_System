    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:

        try{
            String M_Code = JMCode.getText();
            String S_Code = JSCode.getText();
            String I_No = JINo.getText();
            short Quantity = Short.parseShort(Jquantity.getValue().toString());
            double P_Price = Double.parseDouble(JPPrice.getText());

            double Amount = P_Price * Quantity;

            DefaultTableModel DTM = (DefaultTableModel) JTable.getModel();
            DTM.addRow(new Object[]{I_No,M_Code,S_Code,Quantity,P_Price,Amount});

            double total = calculateTotal(DTM);
            JTotal.setText(String.valueOf(total));

            JOptionPane.showMessageDialog(this, "Order added successfully.");
            ClearFields();

        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Invalid input. Please enter valid numbers.");
        }
    }//GEN-LAST:event_AddButtonActionPerformed

